class Movie {
  constructor(id, name, type, language, genres, rating, image, summary, likes) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.language = language;
    this.genres = genres;
    this.rating = rating;
    this.image = image;
    this.summary = summary;
    this.likes = likes;
  }
}

export default Movie;
